export const mockPayrolls: Payroll[] = mockWorkers
  .filter(w => w.status === 'active')
  .map((worker, index) => {
    const calc = calculateSalary(worker.basic_salary);
    return {
      id: `payroll-${index + 1}`,
      worker_id: worker.id,
      worker_name: worker.full_name,
      worker_position: worker.job_position,
      worker_nic: worker.nic_no,
      month: 'December',
      year: 2024,
      basic_salary: calc.basic_salary,
      epf_employee: calc.epf_employee,
      etf_employer: calc.etf_employer,
      net_salary: calc.net_salary,
      payment_method: index % 2 === 0 ? 'bank_transfer' : 'cash',
      paid_date: '2024-12-05',
      paid_status: 'paid',
      created_at: '2024-12-01',
    };
  });

